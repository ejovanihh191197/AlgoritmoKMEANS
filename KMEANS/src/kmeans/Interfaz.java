    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        int x = Integer.parseInt(jTextField2.getText());
        int y = Integer.parseInt(jTextField3.getText());
        if (x < 501 && y < 501) {
            panel.setX1(x);
            panel.setY1(y);
            panel.limpiaCentroide();
            panel.limpiaMuestras();
            panel.setMuestras(Integer.parseInt(jTextField1.getText()));
            panel.init();
            clase = 0;
            jLabel3.setText(String.valueOf(clase));
            panel.removeAll();
            panel.setSize(x, y);
            panel.repaint();
        } else {
            JOptionPane.showMessageDialog(null, "500 Máximo en Rango de X y Y", "Atención", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

