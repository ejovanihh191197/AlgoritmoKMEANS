    private void formMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMousePressed
        // TODO add your handling code here:
        
        int x1 = Integer.parseInt(jTextField2.getText());
        int y1 = Integer.parseInt(jTextField3.getText());
        if (clase < 10) {
            if (evt.getX() < x1 && evt.getY() < y1) {
                clase++;
                x = new Centroide();
                x.setcCoordX(evt.getX()-15);
                x.setcCoordY(evt.getY()-38);
                x.setcClase(clase);
                panel.tomaCentroide(x);
                panel.repaint();
                jLabel3.setText(String.valueOf(clase));
            }

        } else {
            JOptionPane.showMessageDialog(null, "10 Clases Máximo", "Atención", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_formMousePressed

